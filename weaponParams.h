#pragma once

// =====================================================
// WeaponParams : 武器の挙動パラメータ
// =====================================================
// 弾の性質ではなく「武器そのものの動き」をまとめた構造体。
// 連射速度・弾倉・リロードなど、武器の操作感に関わる値を持つ。
struct WeaponParams
{
    int   magazineSize; // 弾倉の最大装弾数。-1 にすると弾数無制限（リロードなし）
    float fireRate;     // 発射間隔（秒）。小さいほど連射が速い。0.25f なら1秒に4発
    float reloadTime;   // リロードにかかる時間（秒）
    float zoomRatio;    // ADS（照準）時の画角倍率。1.0f でズームなし
    bool  singleShot;   // true のとき押した瞬間のみ発射（ホールドでは連射しない）
};
