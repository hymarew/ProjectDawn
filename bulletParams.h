#pragma once

// =====================================================
// BulletParams : 弾の性能パラメータ
// =====================================================
// 武器ごとに異なる「弾自体の性質」をまとめた構造体。
// Weapon の派生クラスで値を設定して使う。
struct BulletParams
{
    float damage;        // 1発あたりのダメージ量
    float speed;         // 弾の移動速度（単位/秒）
    float lifeTime;      // 弾が消えるまでの時間（秒）。これが実質的な射程になる
    float spreadAngle;   // 弾の拡散角（度）。0.0fで完全な直線、大きいほど広がる
    int   bulletPerShot; // 1回の発射で出る弾の数。ショットガンなら複数になる
};
